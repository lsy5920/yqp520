/**
 * 创建题目选项
 * 用途：把选项编号和选项文字统一整理成题库可复用的结构
 * 入参：items 为按顺序传入的选项编号和选项文字
 * 返回值：返回标准化后的选项数组
 */
function createOptions(items: Array<[string, string]>): AssessmentOption[] {
  return items.map(([label, text]) => ({
    id: label,
    label,
    text,
  }))
}

